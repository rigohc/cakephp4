    <h2 class='text-info'>¿Quienes Somos?</h2>
        <p>
        Cada uno de nosotros tiene sus propios desafíos, objetivos y razones para rodar. En BikeStore, 
        nuestro propósito es ayudarte a liberar tu potencial sin importar el camino elegido. Hacemos esto 
        con nuestros productos, nuestra gente y las historias que compartimos. Ven a sentir lo que es no tener 
        límites. Ven a rodar con nosotros.

        BikeStore es la marca líder mundial de bicicletas y accesorios de ciclismo de alta calidad. Como parte de 
        BikeStore Group, fundado en 1972, combina artesanía, tecnología y diseño innovador.

        BikeStore ha sido durante mucho tiempo uno de los principales catalizadores del cambio en el ciclismo. 
        Introdujimos marcos de aluminio más ligeros y resistentes cuando el estándar de la industria era el acero. 
        Fuimos los primeros en hacer que las bicicletas de fibra de carbono estuvieran ampliamente disponibles en 
        todo el mundo. Definimos el aspecto y la sensación de las bicicletas de ruta modernas con nuestra tecnología 
        Compact Road. Y revolucionamos las bicicletas de rendimiento todoterreno con Maestro Suspension.

        Hoy, este espíritu de innovación es más fuerte que nunca. Nuestra colección de accesorios y ropa, desarrollada
        y probada por algunos de los mejores equipos y atletas en carreras profesionales, continúa expandiéndose. 
        Nuestras bicicletas eléctricas líderes en la industria están redefiniendo lo que es posible para ciclistas de
        todos los niveles.

        Estas innovaciones tienen un propósito más amplio. Ganar carreras y producir bicicletas más ligeras, más fuertes
         y más rápidas es importante, pero hay más que eso. Creemos que el viaje puede ser tan satisfactorio como el 
         logro, y que nada supera la sensación de experimentar nuestros éxitos juntos.

        Somos constructores e innovadores, pero también somos una comunidad global de ciclistas. Somos deportistas,
        aventureros y defensores del ciclismo. Somos corredores del Tour de France, exploradores de pistas, vecinos y
        amigos.

        BikeStore es un ecosistema completo de bicicletas, accesorios y servicios de ciclismo, perfectamente conectados y
        siempre accesibles. Puedes encontrarnos en nuestras más de 12.000 tiendas en todo el mundo o conectarte con nosotros
        en línea desde donde te encuentres.
        </p>
